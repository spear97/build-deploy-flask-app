def summation(a, b):
    """
    Function to perform summation of two numbers.

    Args:
        a (float): First number.
        b (float): Second number.

    Returns:
        float: The result of a + b.
    """
    result = a + b
    return result


def subtraction(a, b):
    """
    Function to perform subtraction of two numbers.

    Args:
        a (float): First number.
        b (float): Second number.

    Returns:
        float: The result of a - b.
    """
    result = a - b
    return result


def multiplication(a, b):
    """
    Function to perform multiplication of two numbers.

    Args:
        a (float): First number.
        b (float): Second number.

    Returns:
        float: The result of a * b.
    """
    result = a * b
    return result
